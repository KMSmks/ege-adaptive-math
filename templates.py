def _t7_radical_diff():
    # (√p - √q)(√p + √q) = p - q. Сверка: (√13-√7)(√13+√7) = 6.
    p = random.randint(8, 40)
    q = random.randint(2, p - 1)
    return f"Найдите значение выражения (√{p} − √{q})(√{p} + √{q}).", str(p - q)


def _t7_log_power():
    # k * log_a(a^(m)) = k*m. Сверка-аналог: 6*log_7(7^(1/3))=2 -> здесь целые степени.
    a = random.choice([2, 3, 5, 7])
    k = random.choice([2, 3, 4, 5, 6])
    m = random.randint(2, 4)
    return f"Найдите значение выражения {k} · log_{a}({a}^{m}).", str(k * m)


