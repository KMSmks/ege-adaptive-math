def _t6_log():
    # log_A(B*x + C) = D  =>  x = (A^D - C)/B. Корень целый по построению.
    A = random.choice([2, 3, 4, 5, 6, 7])
    D = random.randint(1, 3)
    B = random.choice([1, 2, 3, 4, 5])
    x = random.randint(-12, 12)
    C = A ** D - B * x
    sign = "+" if C >= 0 else "-"
    text = f"Найдите корень уравнения log_{A}({_lin(B)} {sign} {abs(C)}) = {D}."
    return text, str(x)


def _t6_recip():
    # 1/(k x + m) = 1/(p x + q)  =>  x = (q - m)/(k - p). Сверка: 2x-5=4x+13 -> -9.
    while True:
        k = random.randint(2, 6)
        p = random.randint(2, 6)
        if k == p:
            continue
        x = random.randint(-12, 12)
        m = random.choice([i for i in range(-20, 21) if i != 0])
        q = (k - p) * x + m
        if q == 0 or k * x + m == 0:
            continue
        sm = "+" if m >= 0 else "-"
        sq = "+" if q >= 0 else "-"
        text = (f"Найдите корень уравнения "
                f"1/({_lin(k)} {sm} {abs(m)}) = 1/({_lin(p)} {sq} {abs(q)}).")
        return text, str(x)


def _t6_psq():
    # (x + a)^2 = 4a*x  =>  (x - a)^2 = 0  =>  x = a. Сверка: (x+12)^2=48x -> 12.
    a = random.randint(2, 25)
    return f"Найдите корень уравнения (x + {a})² = {4 * a}x.", str(a)


